/* 

uso do lowercase e do uppercase, dois metodos de string que oferecem utilitarios para formatar texto.

*/ 


let message = "Estou estudando os fundamentos do Javascript"

console.log(message)

//exibir em minusculo 

console.log(message.toLowerCase()) //estou estudando os fundamentos do javascript

//exibir em maiusculo

console.log(message.toUpperCase()) // ESTOU ESTUDANDO OS FUNDAMENTOS DO JAVASCRIPT

